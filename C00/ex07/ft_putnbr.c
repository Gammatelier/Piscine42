#include <unistd.h>

void	ft_putnbr(int nb)
{
	unsigned int i;

	if (nb < 0)
	{
		write(1, "-", 1);
		i = -nb;
	}
	else
		i = nb;
	if (i > 9)
	{
		ft_putnbr(i / 10);
		i = i % 10;
	}
	i = i + '0';
	write(1, &i, 1);
}
